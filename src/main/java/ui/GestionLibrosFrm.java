    private void cargarLibros() {
        List<LibroEntity> libros = libroModel.cargarLibros();
        this.libros = libros; 
        tableModelBooks.setRowCount(0);
        
        for (LibroEntity libro : libros) {
            Object[] row = {
                libro.getId(),
                libro.getTitulo(),
                libro.getAutor(),
                libro.getEstado()
               
            };

            tableModelBooks.addRow(row);

        }
    }
